function SensorContainer() {
  const [sensors, setSensors] = useState([]);
  const [loadingData, setLoadingData] = useState(true);
  useEffect(() => {
    console.log('componentDidMount');
    console.log(sensors);
    async function getData() {
      await axios
        .get('http://localhost:8585/v1/atm/getAuctions')
        .then((response) => {
          // check if the data is populated
          console.log(response.data);
          //   setData(response.data);
          setSensors(response.data);
          // you tell it that you had the result
          setLoadingData(false);
        });
    }
    if (loadingData) {
      getData();
    }
  }, []); // This is self is componentDidMount

  const columns = React.useMemo(
    () => [
      {
        Header: 'ID',
        accessor: 'id',
      },
      {
        Header: 'Name',
        accessor: 'sum_lot',
      },
      {
        Header: 'Serial Number',
        accessor: 'serialNum',
      },
      {
        Header: 'Status',
        accessor: 'status',
      },
    ],
    []
  );

  if (sensors.length === 0 && !loadingData) {
    return <div>No Senors data available</div>;
  }

  return (
    <div>
      {loadingData && <span>Please wait we are fetching data</span>}
      <SensorTable columns={columns} data={sensors} />
    </div>
  );
}
export default function App() {
  return <SensorContainer />;
}
